<p>
    Fakultet informacionih tehnologija – FIT, nastao je 2005. godine. Izgrađen je po principima koje definiše Bolonjska
    deklaracija za evropski sistem obrazovanja. Nastavni programi u oblasti informacionih tehnologija su usaglašeni sa
    preporukama američkih informatičkih profesionalnih udruženja IEEE i ACM, koje usmeravaju programe američkih
    univerziteta u informatičkim oblastima, a u skladu sa potrebama preduzeća, vladinih i drugih organizacija.
    Zahvaljujući toj činjenici diploma FIT-a je lako prepoznatljiva u svim krajevima sveta.


    Kao pionir u uvođenju nastave na daljinu, FIT je omogućio, po prvi put u Srbiji, studije preko Interneta studentima
    koji ne žive u Beogradu ili zbog posla nisu u mogućnosti da prate nastavu na tradicionalan način. Savremeno tržište
    diktira potrebu za usmerenim obrazovanjem budućih zaposlenih. FIT je uvek pratio te potrebe i u prvim godinama svog
    postojanja, pored Informacionih tehnologija, razvio još šest različitih studijskih programa iz oblasti menadžmenta i
    dizajna. Niz specifičnosti u načinu studiranja na FIT-u izgradili su prepoznatljivost koja se preslikala na moćniju
    instituciju, Univerzitet Metropolitan.
